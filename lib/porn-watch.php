/**
 * WordPress Bootstrap Pagination
 */
function wp_bootstrap_pagination( $args = array() ) {

    $defaults = array(
        'range'           => 10,
        'custom_query'    => FALSE,
        'previous_string' => 'Anterior',
        'next_string'     => 'Próxima',
        'before_output'   => '<div class="col-xs-12"><div class="post-nav"><ul class="pager">',
        'after_output'    => '</ul></div></div><!-- /.col-flex -->'
    );

    $args = wp_parse_args(
        $args,
        apply_filters( 'wp_bootstrap_pagination_defaults', $defaults )
    );

    $args['range'] = (int) $args['range'] - 1;
    if ( !$args['custom_query'] )
        $args['custom_query'] = @$GLOBALS['wp_query'];
    $count = (int) $args['custom_query']->max_num_pages;
    $page  = intval( get_query_var( 'paged' ) );
    $ceil  = ceil( $args['range'] / 2 );

    if ( $count <= 1 )
        return FALSE;

    if ( !$page )
        $page = 1;

    if ( $count > $args['range'] ) {
        if ( $page <= $args['range'] ) {
            $min = 1;
            $max = $args['range'] + 1;
        } elseif ( $page >= ($count - $ceil) ) {
            $min = $count - $args['range'];
            $max = $count;
        } elseif ( $page >= $args['range'] && $page < ($count - $ceil) ) {
            $min = $page - $ceil;
            $max = $page + $ceil;
        }
    } else {
        $min = 1;
        $max = $count;
    }

    $echo = '';
    $previous = intval($page) - 1;
    $previous = esc_attr( get_pagenum_link($previous) );

    $firstpage = esc_attr( get_pagenum_link(1) );
    if ( $firstpage && (1 != $page) )
        $echo .= '<li class="previous hidden-xs"><a href="' . $firstpage . '">Primeira</a></li>';
    if ( $previous && (1 != $page) )
        $echo .= '<li><a href="' . $previous . '" title="Anterior"><i class="fa fa-chevron-left"></i></a></li>';

    if ( !empty($min) && !empty($max) ) {
        for( $i = $min; $i <= $max; $i++ ) {
            if ($page == $i) {
                $echo .= '<li class="active"><span class="active">' . str_pad( (int)$i, 2, '0', STR_PAD_LEFT ) . '</span></li>';
            } else {
                $echo .= sprintf( '<li><a href="%s">%002d</a></li>', esc_attr( get_pagenum_link($i) ), $i );
            }
        }
    }

    $next = intval($page) + 1;
    $next = esc_attr( get_pagenum_link($next) );
    if ($next && ($count != $page) )
        $echo .= '<li><a href="' . $next . '" title="' . $args['next_string'] . '"><i class="fa fa-chevron-right"></i></a></li>';

    $lastpage = esc_attr( get_pagenum_link($count) );
    if ( $lastpage ) {
        $echo .= '<li class="next hidden-xs"><a href="' . $lastpage . '">Última</a></li>';
    }
    if ( isset($echo) )
        echo $args['before_output'] . $echo . $args['after_output'];
}




//#? Related Posts
function  related_posts(){

  //VARIABLES
  $orig_post = $post;
  global $post;
  $categories = get_the_category($post->ID);

  if ($categories) {
    $category_ids = array();
    foreach($categories as $individual_category) $category_ids[] = $individual_category->term_id;
    $args=array(
      'category__in' => $category_ids,
      'post__not_in' => array($post->ID),
      'posts_per_page'=> 32, // Number of related posts that will be displayed.
      'caller_get_posts'=>1,
      'orderby'=>'rand' // Randomize the posts
    );
    $my_query = new wp_query( $args );
    if( $my_query->have_posts() ) {
    echo '<section id="related-posts">
            <div class="row row-eq-height">
              <div class="page-header col-xs-12">
                <h3>Vídeos relacionados</h3>
              </div>';
    while( $my_query->have_posts() ) {
    $my_query->the_post();
    //#! Include post content
    include(get_template_directory().'/templates/content.php');
     }
    echo '  </div><!-- /.row -->
          </section>';
    }
  }
  $post = $orig_post;
  wp_reset_query();

}
